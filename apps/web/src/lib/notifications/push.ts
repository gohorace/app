// ─────────────────────────────────────────────────────────────────────────────
// HOR-233 — Portal enquiry alert
//
// Fires when a buyer submits an enquiry on REA / Domain and the inbound
// router (lib/inbound/router.ts) resolves it to a contact. Copy is the
// brief's verbatim line for `portal_enquiry_received` (docs/alerts-copy-
// standards.md → HOR-78): names the contact, the listing and the source,
// and teaches the reply-via-Horace behaviour that binds the device.
//
// The in-app feed entry mirrors this push verbatim (logAlert stores
// title/body, which to-stream-moment renders as headline/editorial).
// ─────────────────────────────────────────────────────────────────────────────

function portalLabel(sourcePortal: string | null): string {
  switch (sourcePortal) {
    case 'rea':    return 'REA'
    case 'domain': return 'Domain'
    default:       return 'a portal'
  }
}

export async function sendPortalEnquiryAlert(
  agentId: string,
  contactId: string,
  /** Enquirer's name as parsed from the email; may be empty for anonymous enquirers. */
  contactName: string | null,
  /** Listing the enquiry was about, e.g. "12 Maple St". */
  listingAddress: string | null,
  sourcePortal: string | null,
): Promise<void> {
  const firstName = (contactName ?? '').trim().split(' ')[0] || null
  const listing = listingAddress?.trim() || 'your listing'
  const via = portalLabel(sourcePortal)

  const title = firstName
    ? `${firstName} just enquired on ${listing} via ${via}`
    : `New enquiry on ${listing} via ${via}`
  const body = firstName
    ? `Horace has set ${firstName} up — first reply via Horace and he'll start watching properly.`
    : `Horace has set them up — first reply via Horace and he'll start watching properly.`

  await dispatchPushAlert({
    agentId,
    contactId,
    type: 'alert_portal_enquiry',
    payload: {
      title,
      body,
      url: `/contacts/${contactId}`,
      tag: `portal-enquiry-${contactId}`,
    },
  })
}

